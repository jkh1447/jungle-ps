int n, m;
char board[101][101];
int dist[101][101];
int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cin >> n >> m;
    for (int i = 0; i < 101; i++)
        fill(dist[i], dist[i] + 101, -1);

    for (int i = 0; i < n; i++) {
        string s;
        cin >> s;
        for (int j = 0; j < m; j++) {
            board[i][j] = s[j];
        }
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if (board[i][j] == 'c') {
                dist[i][j] = 0;
                for (int k = j + 1; k < m; k++) {
                    if (board[i][k] == 'c') break;
                    dist[i][k] = dist[i][k - 1] + 1;
                }
            }
        }
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            cout << dist[i][j] << " ";
        }
        cout << "\n";
    }
}

